<h2>Carta de Serviços</h2>

<br/>
<p><strong>Apresentação</strong><br /><br/>

  A <em>Ouvidoria</em> é um canal de comunicação com  os usuários e um instrumento de gestão participativa. Atua com isenção e ética na análise e encaminhamento das manifestações (<em>elogios, sugestões, denúncias, informações, solicitações e reclamações</em>)  dos usuários relativas às atividades da FIPECq Vida, visando ao aprimoramento  institucional, a ampliação da gestão participativa e o controle social.</p>
  <br/>
  
<p><strong>Carta de Serviços ao  Usuário</strong><br /><br/>

    A Carta de Serviço ao Usuário da FIPECq Vida foi  elaborada com o objetivo de informar ao usuário dos serviços prestados pela <em>Ouvidoria FIPECq Vida</em>, da forma de  acessá-los, dos compromissos e padrões de qualidade de atendimento ao público.</p>
    
    <br/>
    
<p><strong>O Ouvidor - FIPECq Vida</strong><br /><br/>
O  Ouvidor – FIPECq Vida é nomeado pelo Diretor-Presidente, referendado pelo Comitê-Gestor, para mandato de um ano, podendo ser renovável por igual  período. Ao Ouvidor são asseguradas a autonomia e independência de ação,  sendo-lhe franqueado acesso livre a qualquer dependência ou funcionário da  empresa, bem como a informações, registros, processos e documentos de qualquer  natureza que, a seu exclusivo juízo, repute necessários ao pleno exercício de  suas funções, atuando segundo princípios éticos, pautando seu trabalho pela  legalidade, legitimidade, imparcialidade, moralidade, probidade, publicidade e  eficiência.<br /><br/>

  O Ouvidor deverá agir sempre contra  a usurpação de direitos ou abuso de poder; contra erros ou omissões;  negligencias; decisões injustas, tornando a administração mais eficiente e  transparente.</p>

<br/>

<p><strong>Compromisso da Ouvidoria FIPECq Vida  no atendimento ao usuário</strong></p>
<br/>
<ul>
  <li>A <em>Ouvidoria FIPECq Vida</em> deve executar suas  atribuições e competências sempre com visibilidade, transparência das ações,  maior aproximação com os usuários, qualidade dos serviços e produtos ofertados,  atendimento ético e resolutivo;<strong> </strong></li>
  <li>O atendimento aos  usuários será feito de maneira acolhedora, dialogada, informativa, clara e  objetiva, respeitando a confidencialidade, a imparcialidade e a legalidade;<strong> </strong></li>
  <li>A <em>Ouvidoria</em> disponibilizará o acesso aos  serviços prestados em dias úteis, no horário de 8h às 13h e de 14h às 18h;<strong> </strong></li>
  <li>A <em>Ouvidoria</em> tem até 10 dias úteis para  responder, a contar do recebimento da solicitação, estendendo-se o prazo,  devidamente justificado pela área, caso haja necessidade.&nbsp;Neste caso, o  requerente será informado da prorrogação;<strong></strong></li>
  <li>O usuário que  demande a <em>Ouvidoria</em> será informado  de sua resposta pelo canal escolhido no momento de registro da  comunicação;</li>
  <li>Havendo necessidade  de realizar diligências de instrução, ou seja, pedido de dados ou informações  para melhor caracterização da consulta, esse prazo começará a contar a partir  de seu atendimento.<strong></strong></li>
  <li>A <em>Ouvidoria FIPECq Vida</em> deve disponibilizar e atualizar constantemente as informações de interesse público ou coletivo no  sítio eletrônico disponibilizado no site da FIPECq Vida.</li>
</ul>

